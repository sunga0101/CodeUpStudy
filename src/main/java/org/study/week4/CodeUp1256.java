package org.study.week4;

import java.util.Scanner;

public class CodeUp1256 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int iCnt = sc.nextInt();
        for (int i = 0; i < iCnt; i++) {
            System.out.print("*");
        }
        System.out.print("\n");
    }
}

